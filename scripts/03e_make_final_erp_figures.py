def load_yaml(path: Path) -> dict:
    if not path.exists() or yaml is None:
        return {}
    with path.open("r", encoding="utf-8") as f:
        obj = yaml.safe_load(f)
    return obj or {}


def ensure_dir(path: Path) -> None:
    path.mkdir(parents=True, exist_ok=True)


def get_project_root(config: dict) -> Path:
    for key in ["root", "root_dir", "data_root", "bids_root", "project_root"]:
        if config.get(key):
            return Path(config[key])
    return Path.cwd()


def get_windows(config: dict) -> Dict[str, List[float]]:
    windows = (
        config.get("erp_windows_ms")
        or config.get("erp_windows")
        or config.get("windows")
        or DEFAULT_WINDOWS_MS
    )
    windows = {str(k): list(v) for k, v in windows.items()}
    if "W5" not in windows:
        windows["W5"] = [600, 800]
    return windows


def find_first_existing(paths: Iterable[Path]) -> Optional[Path]:
    for p in paths:
        if p.exists():
            return p
    return None


def infer_column(df: pd.DataFrame, candidates: Iterable[str], required: bool = True) -> Optional[str]:
    lower = {c.lower(): c for c in df.columns}
    for c in candidates:
        if c.lower() in lower:
            return lower[c.lower()]
    # Partial matching fallback
    for c in df.columns:
        cl = c.lower()
        for cand in candidates:
            if cand.lower() in cl:
                return c
    if required:
        raise ValueError(f"Could not infer required column. Candidates: {list(candidates)}. Available: {list(df.columns)}")
    return None


def read_grand_average_csv(path: Path) -> pd.DataFrame:
    df = pd.read_csv(path)

    time_col = infer_column(df, ["time_ms", "time", "times", "t_ms", "time_from_stimulus_ms"])
    mean_col = infer_column(df, ["mean_difference", "grand_mean", "mean", "difference", "diff_mean", "y"])
    sem_col = infer_column(df, ["sem", "sem_difference", "sem_average", "grand_sem", "stderr", "se"], required=False)

    out = pd.DataFrame({
        "time_ms": pd.to_numeric(df[time_col], errors="coerce"),
        "mean_difference": pd.to_numeric(df[mean_col], errors="coerce"),
    })

    if sem_col is not None:
        out["sem"] = pd.to_numeric(df[sem_col], errors="coerce")
    else:
        out["sem"] = np.nan

    out = out.dropna(subset=["time_ms", "mean_difference"]).sort_values("time_ms")
    return out


def grand_average_path(diff_dir: Path, modality: str, contrast: str, roi: str) -> Path:
    return diff_dir / f"{modality}_{contrast}_{roi}_grand_average.csv"


def safe_float(x) -> float:
    try:
        return float(x)
    except Exception:
        return np.nan


def bh_fdr(pvals: Iterable[float]) -> np.ndarray:
    p = np.asarray(list(pvals), dtype=float)
    out = np.full_like(p, np.nan, dtype=float)
    valid = np.isfinite(p)
    if valid.sum() == 0:
        return out
    pv = p[valid]
    order = np.argsort(pv)
    ranked = pv[order]
    m = len(ranked)
    adj = ranked * m / np.arange(1, m + 1)
    adj = np.minimum.accumulate(adj[::-1])[::-1]
    adj = np.clip(adj, 0, 1)
    corrected = np.empty_like(adj)
    corrected[order] = adj
    out[valid] = corrected
    return out


def paired_stats_for_w4_w5(subject_diffs: pd.DataFrame) -> pd.DataFrame:
    rows = []
    df = subject_diffs.copy()
    df["subject_id"] = df["subject_id"].astype(str)

    for modality, roi, label in KEY_EFFECTS:
        sub = df[
            (df["contrast"] == "face_vs_nonface")
            & (df["modality"] == modality)
            & (df["roi"] == roi)
            & (df["window"].isin(["W4", "W5"]))
        ].copy()

        if sub.empty:
            continue

        wide = sub.pivot_table(index="subject_id", columns="window", values="difference", aggfunc="mean")
        if "W4" not in wide.columns or "W5" not in wide.columns:
            continue

        wide = wide.dropna(subset=["W4", "W5"])
        if len(wide) == 0:
            continue

        diff = wide["W4"] - wide["W5"]
        n = len(diff)
        mean_w4 = safe_float(wide["W4"].mean())
        mean_w5 = safe_float(wide["W5"].mean())
        mean_drop = safe_float(diff.mean())
        sem_drop = safe_float(diff.std(ddof=1) / math.sqrt(n)) if n > 1 else np.nan

        if stats is not None and n > 1 and diff.std(ddof=1) > 0:
            t_res = stats.ttest_1samp(diff, 0.0)
            p_t = safe_float(t_res.pvalue)
            t_stat = safe_float(t_res.statistic)
            try:
                w_res = stats.wilcoxon(diff)
                p_w = safe_float(w_res.pvalue)
                w_stat = safe_float(w_res.statistic)
            except Exception:
                p_w = np.nan
                w_stat = np.nan
        else:
            t_stat = np.nan
            p_t = np.nan
            w_stat = np.nan
            p_w = np.nan

        n_pos = int((diff > 0).sum())
        n_neg = int((diff < 0).sum())
        dz = safe_float(diff.mean() / diff.std(ddof=1)) if n > 1 and diff.std(ddof=1) > 0 else np.nan

        rows.append({
            "modality": modality,
            "roi": roi,
            "label": label,
            "n_subjects": n,
            "mean_w4": mean_w4,
            "mean_w5": mean_w5,
            "mean_w4_minus_w5": mean_drop,
            "sem_w4_minus_w5": sem_drop,
            "n_w4_gt_w5": n_pos,
            "n_w4_lt_w5": n_neg,
            "positive_pct": 100 * n_pos / n if n else np.nan,
            "t_stat": t_stat,
            "p_ttest": p_t,
            "wilcoxon_stat": w_stat,
            "p_wilcoxon": p_w,
            "effect_size_dz": dz,
        })

    out = pd.DataFrame(rows)
    if len(out) > 0:
        out["p_fdr_ttest"] = bh_fdr(out["p_ttest"])
        out["p_fdr_wilcoxon"] = bh_fdr(out["p_wilcoxon"])
    return out


def label_for_pair(modality: str, roi: str) -> str:
    for m, r, label in KEY_EFFECTS:
        if m == modality and r == roi:
            return label
    return f"{modality}\n{roi}"


def add_windows_background(ax, windows: Dict[str, List[float]]) -> None:
    # Mark W4 and W5 most clearly; keep W1-W3 as light labels.
    if "W4" in windows:
        ax.axvspan(windows["W4"][0], windows["W4"][1], alpha=0.10, zorder=0)
        ytop = ax.get_ylim()[1]
        ax.text(np.mean(windows["W4"]), ytop * 0.94, "W4", ha="center", va="top", fontsize=9)
    if "W5" in windows:
        ax.axvspan(windows["W5"][0], windows["W5"][1], alpha=0.06, zorder=0)
        ytop = ax.get_ylim()[1]
        ax.text(np.mean(windows["W5"]), ytop * 0.94, "W5", ha="center", va="top", fontsize=9)
    for w in ["W1", "W2", "W3"]:
        if w in windows:
            ytop = ax.get_ylim()[1]
            ax.text(np.mean(windows[w]), ytop * 0.94, w, ha="center", va="top", fontsize=8)



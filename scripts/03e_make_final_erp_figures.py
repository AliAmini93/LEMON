def figure_difference_waves(diff_dir: Path, out_dir: Path, windows: Dict[str, List[float]]) -> pd.DataFrame:
    source_rows = []

    fig, axes = plt.subplots(2, 2, figsize=(12, 7), sharex=True)
    axes = axes.flatten()

    for ax, panel in zip(axes, DIFF_WAVE_PANELS):
        p = grand_average_path(
            diff_dir,
            panel["modality"],
            panel["contrast"],
            panel["roi"],
        )

        if not p.exists():
            ax.set_title(f"Missing: {p.name}", fontsize=10)
            ax.axis("off")
            continue

        df = read_grand_average_csv(p)
        time = df["time_ms"].to_numpy()
        y = df["mean_difference"].to_numpy()
        sem = df["sem"].to_numpy()

        ax.plot(time, y, linewidth=2.0, label="Face - Non-face")
        if np.isfinite(sem).any():
            ax.fill_between(time, y - sem, y + sem, alpha=0.20, linewidth=0, label="± SEM")

        ax.axhline(0, linestyle="--", linewidth=1)
        ax.axvline(0, linestyle=":", linewidth=1)
        ax.set_title(panel["title"], fontsize=12)
        ax.set_xlim(-200, 800)
        ax.set_xlabel("Time from stimulus onset (ms)")
        ax.set_ylabel("Difference amplitude")
        add_windows_background(ax, windows)
        ax.legend(loc="upper left", fontsize=8, frameon=False)

        for _, r in df.iterrows():
            source_rows.append({
                "figure": "figure_01_face_nonface_difference_waves",
                "modality": panel["modality"],
                "contrast": panel["contrast"],
                "roi": panel["roi"],
                "time_ms": r["time_ms"],
                "mean_difference": r["mean_difference"],
                "sem": r["sem"],
                "source_file": str(p),
            })

    fig.suptitle("Face - Non-face ERP difference waves", fontsize=16)
    fig.tight_layout(rect=[0, 0, 1, 0.96])

    out_png = out_dir / "figure_01_face_nonface_difference_waves.png"
    out_pdf = out_dir / "figure_01_face_nonface_difference_waves.pdf"
    fig.savefig(out_png, dpi=300)
    fig.savefig(out_pdf)
    plt.close(fig)

    print(f"[OK] Wrote: {out_png}")
    print(f"[OK] Wrote: {out_pdf}")

    return pd.DataFrame(source_rows)


def figure_subject_w4_boxplot(subject_diffs: pd.DataFrame, out_dir: Path) -> None:
    df = subject_diffs[
        (subject_diffs["contrast"] == "face_vs_nonface")
        & (subject_diffs["window"] == "W4")
    ].copy()

    groups = []
    labels = []
    for modality, roi, label in KEY_EFFECTS:
        vals = df[(df["modality"] == modality) & (df["roi"] == roi)]["difference"].dropna().to_numpy()
        if len(vals) > 0:
            groups.append(vals)
            labels.append(label)

    fig, ax = plt.subplots(figsize=(10, 5.5))

    bp = ax.boxplot(groups, tick_labels=labels, showmeans=True, patch_artist=True)
    for patch in bp["boxes"]:
        patch.set_alpha(0.35)

    for i, vals in enumerate(groups, start=1):
        jitter = np.linspace(-0.08, 0.08, len(vals)) if len(vals) > 1 else np.array([0.0])
        ax.scatter(np.full(len(vals), i) + jitter, vals, s=26, alpha=0.75, zorder=3)

    ax.axhline(0, linestyle="--", linewidth=1)
    ax.set_title("Subject-level Face - Non-face differences in W4 (320-600 ms)", fontsize=14)
    ax.set_ylabel("Subject-level difference")
    ax.tick_params(axis="x", labelrotation=30)
    fig.tight_layout()

    out_png = out_dir / "figure_02_subject_level_w4_boxplot.png"
    out_pdf = out_dir / "figure_02_subject_level_w4_boxplot.pdf"
    fig.savefig(out_png, dpi=300)
    fig.savefig(out_pdf)
    plt.close(fig)

    print(f"[OK] Wrote: {out_png}")
    print(f"[OK] Wrote: {out_pdf}")


def figure_w4_w5_summary(w4w5: pd.DataFrame, out_dir: Path) -> None:
    df = w4w5.copy()
    rows = []
    for modality, roi, label in KEY_EFFECTS:
        sub = df[(df["modality"] == modality) & (df["roi"] == roi)]
        if len(sub) == 0:
            continue
        r = sub.iloc[0].to_dict()
        r["label"] = label
        rows.append(r)

    plot_df = pd.DataFrame(rows)
    if plot_df.empty:
        print("[WARN] No rows available for W4/W5 summary figure.")
        return

    x = np.arange(len(plot_df))
    width = 0.36

    fig, ax = plt.subplots(figsize=(10, 5.5))
    ax.bar(
        x - width / 2,
        plot_df["w4_mean_difference"],
        width,
        yerr=plot_df.get("w4_sem_average", np.nan),
        capsize=4,
        label="W4: 320-600 ms",
    )
    ax.bar(
        x + width / 2,
        plot_df["w5_mean_difference"],
        width,
        yerr=plot_df.get("w5_sem_average", np.nan),
        capsize=4,
        label="W5: 600-800 ms",
    )

    ax.axhline(0, linestyle="--", linewidth=1)
    ax.set_xticks(x)
    ax.set_xticklabels(plot_df["label"], rotation=30, ha="right")
    ax.set_ylabel("Grand-average difference")
    ax.set_title("Face - Non-face: W4 versus W5", fontsize=14)
    ax.legend(frameon=True)
    fig.tight_layout()

    out_png = out_dir / "figure_03_w4_w5_summary.png"
    out_pdf = out_dir / "figure_03_w4_w5_summary.pdf"
    fig.savefig(out_png, dpi=300)
    fig.savefig(out_pdf)
    plt.close(fig)

    print(f"[OK] Wrote: {out_png}")
    print(f"[OK] Wrote: {out_pdf}")


def figure_w4_w5_paired(subject_diffs: pd.DataFrame, out_dir: Path) -> pd.DataFrame:
    paired_stats = paired_stats_for_w4_w5(subject_diffs)

    # Build plotting table.
    rows = []
    for modality, roi, label in KEY_EFFECTS:
        sub = subject_diffs[
            (subject_diffs["contrast"] == "face_vs_nonface")
            & (subject_diffs["modality"] == modality)
            & (subject_diffs["roi"] == roi)
            & (subject_diffs["window"].isin(["W4", "W5"]))
        ].copy()

        if sub.empty:
            continue

        wide = sub.pivot_table(index="subject_id", columns="window", values="difference", aggfunc="mean")
        if "W4" not in wide.columns or "W5" not in wide.columns:
            continue

        wide = wide.dropna(subset=["W4", "W5"])
        for sid, r in wide.iterrows():
            rows.append({
                "subject_id": sid,
                "modality": modality,
                "roi": roi,
                "label": label,
                "W4": r["W4"],
                "W5": r["W5"],
                "W4_minus_W5": r["W4"] - r["W5"],
            })

    plot_df = pd.DataFrame(rows)
    if plot_df.empty:
        print("[WARN] No rows available for W4/W5 paired figure.")
        return paired_stats

    labels = list(dict.fromkeys(plot_df["label"].tolist()))
    n = len(labels)
    fig, axes = plt.subplots(1, n, figsize=(max(10, n * 2.2), 5), sharey=True)
    if n == 1:
        axes = [axes]

    for ax, label in zip(axes, labels):
        sub = plot_df[plot_df["label"] == label]
        for _, r in sub.iterrows():
            ax.plot([0, 1], [r["W4"], r["W5"]], marker="o", linewidth=1, alpha=0.55)
        ax.scatter([0], [sub["W4"].mean()], marker="D", s=55, zorder=4)
        ax.scatter([1], [sub["W5"].mean()], marker="D", s=55, zorder=4)
        ax.axhline(0, linestyle="--", linewidth=1)
        ax.set_xticks([0, 1])
        ax.set_xticklabels(["W4", "W5"])
        ax.set_title(label, fontsize=10)
        ax.set_xlim(-0.25, 1.25)

    axes[0].set_ylabel("Subject-level difference")
    fig.suptitle("Subject-level W4 to W5 change for Face - Non-face", fontsize=14)
    fig.tight_layout(rect=[0, 0, 1, 0.93])

    out_png = out_dir / "figure_04_w4_w5_paired_subjects.png"
    out_pdf = out_dir / "figure_04_w4_w5_paired_subjects.pdf"
    fig.savefig(out_png, dpi=300)
    fig.savefig(out_pdf)
    plt.close(fig)

    print(f"[OK] Wrote: {out_png}")
    print(f"[OK] Wrote: {out_pdf}")

    return paired_stats


